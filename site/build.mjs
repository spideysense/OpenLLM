// ── escaping ────────────────────────────────────────────────────────────────
const esc = (s) => String(s).replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
const attr = (s) => esc(s).replace(/"/g, '&quot;');
// JSON-LD is embedded in <script>; only "</" needs neutralizing to be safe.
const ld = (obj) => JSON.stringify(obj).replace(/</g, '\\u003c');
const allFaqs = faqGroups.flatMap((g) => g.items);

// ── homepage FAQ (accordions, grouped) ───────────────────────────────────────
function homepageFaqHtml() {
  const out = ['<!-- generated by site/build.mjs from content/knowledge.mjs — do not edit -->'];
  for (const g of faqGroups) {
    out.push(`<div style="font-size:.78rem;font-weight:700;letter-spacing:.08em;text-transform:uppercase;color:var(--gd);margin:2.4rem 0 .6rem">${esc(g.title)}</div>`);
    for (const it of g.items) {
      out.push(
        `<details style="border-bottom:1px solid rgba(93,78,55,.12);padding:1.1rem 0">` +
        `<summary style="font-weight:600;cursor:pointer;font-size:1.02rem">${esc(it.q)}</summary>` +
        `<p style="margin:.7rem 0 0;color:#5d4e37;line-height:1.6">${esc(it.a)}</p></details>`,
      );
    }
  }
  out.push(`<p style="margin:2.6rem 0 0;text-align:center"><a href="/docs" style="color:var(--gd);font-weight:600;text-decoration:none">Read the full documentation &rarr;</a></p>`);
  return out.join('\n');
}

function faqPageLd(url) {
  return ld({
    '@context': 'https://schema.org',
    '@type': 'FAQPage',
    'url': url,
    'mainEntity': allFaqs.map((f) => ({
      '@type': 'Question',
      'name': f.q,
      'acceptedAnswer': { '@type': 'Answer', 'text': f.a },
    })),
  });
}

// ── block renderers (docs) ────────────────────────────────────────────────────
function blockHtml(b) {
  const [t] = b;
  if (t === 'p') return `<p>${esc(b[1])}</p>`;
  if (t === 'h') return `<h3>${esc(b[1])}</h3>`;
  if (t === 'ul') return `<ul>${b[1].map((i) => `<li>${esc(i)}</li>`).join('')}</ul>`;
  if (t === 'ol') return `<ol>${b[1].map((i) => `<li>${esc(i)}</li>`).join('')}</ol>`;
  if (t === 'code') return `<pre><code class="language-${attr(b[1])}">${esc(b[2])}</code></pre>`;
  if (t === 'note') return `<div class="note">${esc(b[1])}</div>`;
  return '';
}
function blockMd(b) {
  const [t] = b;
  if (t === 'p') return b[1];
  if (t === 'h') return `### ${b[1]}`;
  if (t === 'ul') return b[1].map((i) => `- ${i}`).join('\n');
  if (t === 'ol') return b[1].map((i, n) => `${n + 1}. ${i}`).join('\n');
  if (t === 'code') return '```' + b[1] + '\n' + b[2] + '\n```';
  if (t === 'note') return `> ${b[1]}`;
  return '';
}

