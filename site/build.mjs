// ── sitemap ───────────────────────────────────────────────────────────────────
function sitemap() {
  const urls = [
    { loc: URL + '/', changefreq: 'weekly', priority: '1.0' },
    { loc: URL + '/docs', changefreq: 'weekly', priority: '0.9' },
    { loc: URL + '/blog/', changefreq: 'weekly', priority: '0.7' },
    { loc: URL + '/privacy', changefreq: 'yearly', priority: '0.4' },
  ];
  // Enumerate every blog post so each article is in the sitemap, dated from its
  // own article:published_time. New posts are picked up automatically.
  try {
    const dir = join(SITE_DIR, 'blog');
    for (const f of readdirSync(dir).sort()) {
      if (!f.endsWith('.html') || f === 'index.html') continue;
      let lastmod = site.updated;
      try {
        const m = readFileSync(join(dir, f), 'utf8').match(/article:published_time"\s+content="(\d{4}-\d{2}-\d{2})/);
        if (m) lastmod = m[1];
      } catch { /* ignore */ }
      urls.push({ loc: `${URL}/blog/${f}`, changefreq: 'monthly', priority: '0.6', lastmod });
    }
  } catch { /* no blog dir */ }
  return `<?xml version="1.0" encoding="UTF-8"?>
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">
${urls.map((u) => `  <url>
    <loc>${u.loc}</loc>
    <lastmod>${u.lastmod || site.updated}</lastmod>
    <changefreq>${u.changefreq}</changefreq>
    <priority>${u.priority}</priority>
  </url>`).join('\n')}
</urlset>
`;
}

// ── inject into index.html between markers ────────────────────────────────────
function injectBetween(src, startMark, endMark, payload, label) {
  const i = src.indexOf(startMark), j = src.indexOf(endMark);
  if (i === -1 || j === -1) throw new Error(`markers missing for ${label}`);
  return src.slice(0, i + startMark.length) + '\n' + payload + '\n' + src.slice(j);
}

// ── run ───────────────────────────────────────────────────────────────────────
const indexPath = join(SITE_DIR, 'index.html');
let index = readFileSync(indexPath, 'utf8');
index = injectBetween(index, '<!--FAQ:START-->', '<!--FAQ:END-->', homepageFaqHtml(), 'FAQ');
index = injectBetween(index, '<!--FAQLD:START-->', '<!--FAQLD:END-->',
  `<script type="application/ld+json">${faqPageLd(URL + '/#faq')}</script>`, 'FAQLD');
writeFileSync(indexPath, index);

mkdirSync(join(SITE_DIR, 'docs'), { recursive: true });
writeFileSync(join(SITE_DIR, 'docs', 'index.html'), docsPage());
writeFileSync(join(SITE_DIR, 'llms.txt'), llmsTxt());
writeFileSync(join(SITE_DIR, 'llms-full.txt'), llmsFullTxt());
writeFileSync(join(SITE_DIR, 'sitemap.xml'), sitemap());

console.log(`✓ built from knowledge.mjs`);
console.log(`  homepage FAQ: ${allFaqs.length} Q&A across ${faqGroups.length} groups`);
console.log(`  docs page:    ${docs.length} sections -> site/docs/index.html`);
console.log(`  llms.txt, llms-full.txt, sitemap.xml regenerated`);
