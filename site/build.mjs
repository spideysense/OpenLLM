// Reads site/content/knowledge.mjs (the single source of truth) and writes the
// static, crawlable artifacts: the homepage FAQ + its FAQPage JSON-LD, the /docs
// page, llms.txt, llms-full.txt, and sitemap.xml.
//
//   Usage:  node site/build.mjs
//
// Edit knowledge.mjs, run this, commit. Generated regions are marked and should
// not be hand-edited.
// ─────────────────────────────────────────────────────────────────────────────
import { readFileSync, writeFileSync, mkdirSync, readdirSync } from 'node:fs';
